package Class_Examples;

public class ForLoopDemo {
    public static void main(String[] args) {
        forWhtItsWrth();
    }
    public static void forWhtItsWrth() {
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                System.out.println("Count is: " + i + "." +j);
            }

        }
    }
}
